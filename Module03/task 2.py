class_of_a_cruise_ship = input("Enter class of a cruise ship: ")
if class_of_a_cruise_ship=='LUX':
    print("upper-deck cabin with a balcony.")
elif class_of_a_cruise_ship=='A':
    print("above the car deck, equipped with a window.")
elif class_of_a_cruise_ship=='B':
    print("windowless cabin above the car deck.")
elif class_of_a_cruise_ship=='C':
    print("windowless cabin below the car deck.")
else:
    print("Invalid class")


# Output
# Enter class of a cruise ship: C
# windowless cabin below the car deck.